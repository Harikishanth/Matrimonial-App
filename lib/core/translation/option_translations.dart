  if (langCode == 'ta') {
    // 1. Dynamic Height parsing, e.g. "5' 2\" (157 cm)"
    final heightReg = RegExp(r'^(\d+)\x27\s*(\d+)\x22\s*\((\d+)\s*cm\)$');
    final heightMatch = heightReg.firstMatch(option);
    if (heightMatch != null) {
      final ft = heightMatch.group(1);
      final inch = heightMatch.group(2);
      final cm = heightMatch.group(3);
      return "$ft அடி $inch அங்குலம் ($cm செ.மீ)";
    }

    // 2. Dynamic Weight parsing, e.g. "40 kg (88 lbs)"
    final weightReg = RegExp(r'^(\d+)\s*kg\s*\((\d+)\s*lbs\)$');
    final weightMatch = weightReg.firstMatch(option);
    if (weightMatch != null) {
      final kg = weightMatch.group(1);
      final lbs = weightMatch.group(2);
      return "$kg கி.கி ($lbs பவுண்ட்)";
    }

    // 3. Fallback to dictionary
    return optionTranslationsTa[option] ?? geographicTranslationsTa[option] ?? option;
  }
  return option;
}
